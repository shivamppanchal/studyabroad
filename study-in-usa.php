                      <span style="color:#0066FF;"><strong>What tests must I take to be considered for graduate
                          admission?</strong></span><br>


                      <p align="justify">TOEFL (Test of English as a Foreign Language): Almost all U.S. institutions
                        require international applicants whose native language is not English to demonstrate their
                        English language proficiency by taking the TOEFL before they are considered for admission. A
                        new, Internet-based version of the TOEFL test (TOEFL iBT) is being introduced beginning in
                        2006. The TOEFL iBT score assesses speaking, listening, reading, and writing.</p><br>

                      <p align="justify">
                        Graduate Record Examination (GRE): The GRE test is required for students to enroll in master's
                        or doctoral programs in the arts, sciences, humanities, and engineering fields. It consists of
                        two independent tests, the General GRE and the Subject GRE. The General Test is a computer test
                        that measures verbal, quantitative, and analytical writing skills developed over a long period
                        of time and not necessarily related to a particular field of study. The Subject Tests are
                        designed to measure knowledge and understanding of subject matter related to graduate study in
                        specific fields and also to assess whether a student has the prerequisite background to
                        undertake graduate study in a particular field. Since most graduate departments within a
                        university establish their own entrance requirements, including tests, you should write
                        directly to the universities in which you are interested to find out whether the Subject GRE is
                        required and in which subject.</p><br>

                      <p align="justify">
                        Graduate Management Admission Test (GMAT): The GMAT is designed for business schools to assess
                        the qualifications of applicants for advanced study in business and management. It does not
                        test specific knowledge attained through college course-work or achievement in a particular
                        subject area. It measures general verbal, mathematical, and analytical writing skills developed
                        over a long period of time.</p>


